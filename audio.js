function seek(event) {
    // Գտնում ենք .seek-bar էլեմենտը
    let seekBar = document.querySelector('.seek-bar');
    
    // Ստանում ենք նրա ամբողջական լայնությունը պիքսելներով
    let barWidth = seekBar.clientWidth;
    
    // Իմանում ենք, թե ձախ եզրից քանի պիքսել հեռավորության վրա է եղել սեղմումը
    let clickX = event.offsetX;
    
    // Երգի ընդհանուր տևողությունը
    let duration = song.duration;
    
    if (duration) {
        // Հաշվում ենք նոր ժամանակը և փոխում աուդիոյի ընթացիկ պահը
        song.currentTime = (clickX / barWidth) * duration;
    }
}
